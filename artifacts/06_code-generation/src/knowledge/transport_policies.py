"""交通費精算業務ルール（KN-003）"""


def get_transport_policies(deadline_months: int, approval_threshold: int) -> str:
    """交通費精算業務ルールを返す。

    Args:
        deadline_months: 申請期限月数
        approval_threshold: 上長承認閾値（円）

    Returns:
        システムプロンプトに組み込む交通費精算業務ルールテキスト
    """
    return f"""【交通費精算業務ルール】
BRL-08: 申請期限
  → 交通費精算申請は経費発生日（移動日）から{deadline_months}ヶ月以内に申請すること
  → 期限超過時はエラーを通知し業務終了する

BRL-10: 上長承認閾値
  → 交通費合計が{approval_threshold:,}円を超える場合は上長の事前承認が必要
  → 通知後も申請書作成は続行する

BRL-12: 交通費自動計算
  → 電車: 運賃データの経路テーブルから出発駅・到着駅で検索して運賃を取得する
  → バス・タクシー・飛行機: 固定運賃データから運賃を取得する
  → 該当経路が存在しない場合: ユーザーに手動での金額入力を案内する

BRL-15: 駅名正規化
  → 入力された駅名の表記ゆれを正規化する（「渋谷駅」→「渋谷」）
  → calculate_transport_fare ツールを呼び出す前に必ず正規化を行う

BRL-16: 業務目的必須
  → すべての移動区間に業務目的の記載が必要

BRL-17: 移動情報の一括確認収集
  → 移動日・出発地・目的地・交通手段・業務目的を一括で確認収集する
  → 費用は自動計算するため確認しない（運賃データに該当経路が存在しない場合のみ確認する）"""
