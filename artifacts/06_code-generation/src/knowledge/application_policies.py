"""申請種別判断ルール・申請先情報（KN-001, KN-002）

AG-001（申請受付窓口エージェント）が参照する申請ルールを提供する。
他のモジュールに依存しない（純粋なテキスト返却のみ）。
"""


def get_application_policies() -> str:
    """申請種別判断ルールと申請先情報を返す。

    Returns:
        str: システムプロンプトに組み込む申請ルールテキスト
    """
    return """【申請種別判断ルール】
BRL-01: 申請内容に「業務上の交通費精算（電車・バス・タクシー・飛行機）」に関する内容が含まれる場合
  → 交通費精算申請を案内し、transport_agent ツールを呼び出す

BRL-02: 申請内容に「業務上の経費精算（物品購入・宿泊・資格取得等）」に関する内容が含まれる場合
  → 経費精算申請を案内し、expense_agent ツールを呼び出す

BRL-03: 申請種別を判断できない場合
  → 「交通費精算申請」「経費精算申請」の2択を提示し、ユーザーの選択を受け付ける

BRL-05: 申請内容が交通費精算・経費精算のいずれにも該当しない場合
  → 対象外である旨を説明し、担当者への連絡を案内して業務終了する

【判断優先順序】
1. BRL-01（交通費精算申請）の条件を確認する
2. BRL-02（経費精算申請）の条件を確認する
3. いずれにも該当しない場合は BRL-03（判断不能）として選択肢を提示する
4. 明らかに対象外の場合は BRL-05（対象外）としてエスカレーションを案内する

【申請先・申請書情報】
BRL-06: 交通費精算申請の場合
  → 申請書: 「交通費精算申請書」
  → 申請先: 所属部門の経理担当者

BRL-07: 経費精算申請の場合
  → 申請書: 「経費精算申請書」
  → 申請先: 所属部門の経理担当者"""
