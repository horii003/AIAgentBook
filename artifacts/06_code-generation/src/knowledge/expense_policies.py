"""経費精算業務ルール・経費区分判断基準（KN-004, KN-005）

AG-002（経費精算申請エージェント）が参照する業務ルールを提供する。
他のモジュールに依存しない（純粋なテキスト返却のみ）。
"""


def get_expense_policies(deadline_months: int, approval_threshold: int) -> str:
    """経費精算業務ルールを返す。

    Args:
        deadline_months: 申請期限月数（settings.expense.deadline_months から取得）
        approval_threshold: 上長承認閾値（settings.expense.approval_threshold から取得）

    Returns:
        str: システムプロンプトに組み込む経費精算業務ルールテキスト
    """
    return f"""【経費精算業務ルール】
BRL-09: 申請期限
  → 経費精算申請は経費発生日（購入日）から{deadline_months}ヶ月以内に申請すること
  → 期限超過時はエラーを通知し業務終了する

BRL-11: 上長承認閾値
  → 経費合計が{approval_threshold:,}円を超える場合は上長の事前承認が必要
  → 通知後も申請書作成は続行する

BRL-13: 領収書画像からの経費情報自動抽出
  → 提供された領収書画像から購入日・店舗名・品目・金額を品目ごとに自動抽出する

BRL-16: 業務目的必須
  → すべての経費明細に業務目的の記載が必要（空文字禁止）"""


def get_expense_category_policies() -> str:
    """経費区分判断基準を返す。

    Returns:
        str: システムプロンプトに組み込む経費区分判断基準テキスト
    """
    return """【経費区分判断基準（BRL-14）】
品目から以下の経費区分を自動判断する:

事務用品費:
  → 文房具・コピー用紙・印刷物・書籍・オフィス用品等の事務用品

宿泊費:
  → 出張時のホテル・旅館等の宿泊費用

資格精算費:
  → 業務に関連する資格取得・試験費用・受験料・認定費用

その他経費:
  → 上記いずれにも該当しない業務上の経費

【判断不能時の対応】
品目が曖昧で区分を判断できない場合は「その他経費」として分類する"""
